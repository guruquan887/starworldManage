package com.keno8.struts.dto;

public class GradeCSDTO {

	private int id;
	private int grade;
	private long gradeExp;
	private double rebateLV;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}

	public long getGradeExp() {
		return gradeExp;
	}
	public void setGradeExp(long gradeExp) {
		this.gradeExp = gradeExp;
	}
	public double getRebateLV() {
		return rebateLV;
	}
	public void setRebateLV(double rebateLV) {
		this.rebateLV = rebateLV;
	}
	
	

	

	

}
